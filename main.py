@app.route("/edit-post/<int:post_id>", methods=["GET", "POST"])
@admin_only
def edit_post(post_id):
    post = BlogPost.query.get(post_id)
    edit_form = CreatePostForm(obj=post)
    if edit_form.validate_on_submit():
        post.title = edit_form.title.data
        post.subtitle = edit_form.subtitle.data
        post.img_url = edit_form.img_url.data
        post.body = edit_form.body.data
        db.session.commit()
        return redirect(url_for("show_post", post_id=post.id))

    return render_template("make-post.html", form=edit_form, current_year=CURRENT_YEAR, is_edit=True)


@app.route("/delete/<int:post_id>")
@admin_only
def delete_post(post_id):
    post_to_delete = BlogPost.query.get(post_id)
    db.session.delete(post_to_delete)
    db.session.commit()
    return redirect(url_for('get_all_posts'))


def send_email(name, email, phone_num, message):
    with smtplib.SMTP("smtp.gmail.com") as connection:
        connection.starttls()
        connection.login(MY_EMAIL, EMAIL_PASSWORD)
        msg = f"Subject:New Message!\n\n" \
              f"Name: {name}\n" \
              f"Email: {email}\n" \
              f"Phone Number: {phone_num}\n" \
              f"Message: {message}"
        connection.sendmail(from_addr=MY_EMAIL, to_addrs=MY_EMAIL, msg=msg.encode())


